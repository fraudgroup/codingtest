function solution(m, arr) {
  let answer = 0;

  let endPointer = 0;
  let sum = 0;

  for (let startPointer = 0; startPointer < arr.length; startPointer++) {
    while (sum < m && endPointer < arr.length) {
      sum += arr[endPointer++];
    }

    if (sum === m) {
      answer++;
    }

    sum -= arr[startPointer];
  }

  return answer;
}

