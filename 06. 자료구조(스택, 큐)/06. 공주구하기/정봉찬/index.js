function solution(n, k) {
  let answer;
  const queue = Array.from({ length: n }, (_, i) => n - i);
  let count = 0;

  while (queue.length > 1) {
    const el = queue.pop();
    count++;

    if (count < k) {
      queue.unshift(el);
      continue;
    }

    count = 0;
  }

  answer = queue[0];

  return answer;
}

