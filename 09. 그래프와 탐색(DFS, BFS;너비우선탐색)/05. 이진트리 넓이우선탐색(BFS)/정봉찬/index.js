function solution() {
  let answer = '';
  const queue = [];

  function BFS() {
    const node = queue.shift();
    answer += node + ' ';

    if (node === 7) {
      return;
    }

    const left = node * 2;
    const right = node * 2 + 1;

    queue.push(left);
    queue.push(right);

    BFS();
  }

  queue.push(1);
  BFS(0);

  return answer;
}

