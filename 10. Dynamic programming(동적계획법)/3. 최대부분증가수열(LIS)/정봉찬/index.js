function solution(arr) {
  let answer = 0;

  const dy = Array.from({ length: arr.length }, () => 0);

  dy[0] = 1;

  for (let i = 1; i < arr.length; i++) {
    const cur = arr[i];
    let max = 0;

    for (let j = i - 1; j >= 0; j--) {
      const pre = arr[j];

      if (pre >= cur) {
        continue;
      }

      max = Math.max(max, dy[j]);
    }

    dy[i] = max ? max + 1 : 1;
  }

  answer = Math.max(...dy);

  return answer;
}

let arr = [5, 3, 7, 8, 6, 2, 9, 4];
console.log(solution(arr)); // 4
