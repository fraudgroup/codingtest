function solution(arr) {
  let answer = 'NO';
  const ch = Array.from({ length: arr.length }, () => 0);

  function DFS(idx) {
    if (idx === arr.length) {
      let one = 0;
      let two = 0;
      for (let i = 0; i < arr.length; i++) {
        if (ch[i]) {
          one += arr[i];
          continue;
        }

        two += arr[i];
      }

      if (one === two) {
        answer = 'YES';
      }

      return;
    }

    ch[idx] = 1;
    DFS(idx + 1);
    ch[idx] = 0;
    DFS(idx + 1);
  }

  DFS(0);

  return answer;
}

