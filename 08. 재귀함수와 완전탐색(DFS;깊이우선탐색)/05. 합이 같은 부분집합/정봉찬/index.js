function solution2(arr) {
  let answer = 'NO';
  const total = arr.reduce((pre, cur) => pre + cur, 0);
  let isFinished = 0;

  function DFS(idx, sum) {
    if (isFinished) {
      return;
    }

    if (idx === arr.length) {
      if (total - sum === sum) {
        answer = 'YES';
        isFinished = 1;
      }
      return;
    }

    DFS(idx + 1, sum + arr[idx]);
    DFS(idx + 1, sum);
  }

  DFS(0, 0);

  return answer;
}

