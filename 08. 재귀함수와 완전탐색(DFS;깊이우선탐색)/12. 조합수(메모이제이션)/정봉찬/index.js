function solution(n, r) {
  let answer = [];

  function DFS(_n, _r) {
    if (_r > _n) {
      return 0;
    }

    if (_n === _r) {
      return 1;
    }

    if (_r === 1) {
      return _n;
    }

    return DFS(_n - 1, _r - 1) + DFS(_n - 1, _r);
  }

  answer = DFS(n, r);

  return answer;
}

