function solution(c, arr) {
  let answer = Number.MIN_SAFE_INTEGER;
  const ch = Array.from({ length: arr.length }, () => 0);

  function DFS(idx) {
    if (idx === arr.length) {
      let sum = 0;
      for (let i = 0; i < arr.length; i++) {
        if (!ch[i]) {
          continue;
        }

        sum += arr[i];
      }

      if (sum <= c) {
        answer = Math.max(answer, sum);
      }

      return;
    }

    ch[idx] = 1;
    DFS(idx + 1);
    ch[idx] = 0;
    DFS(idx + 1);
  }

  DFS(0);

  return answer;
}

