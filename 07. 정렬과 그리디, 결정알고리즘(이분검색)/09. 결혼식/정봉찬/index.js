function solution(times) {
  let answer = Number.MIN_SAFE_INTEGER;
  const queue = [];

  times.sort((a, b) => a[1] - b[1] || a[0] - b[0]);

  for (let i = 0; i < times.length; i++) {
    while (queue[0]?.[1] <= times[i][0]) {
      queue.shift();
    }

    queue.push(times[i]);

    if (queue.length > answer) {
      answer = queue.length;
    }
  }

  return answer;
}

