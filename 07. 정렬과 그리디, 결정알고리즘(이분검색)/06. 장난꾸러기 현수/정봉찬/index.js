function solution(arr) {
  let answer = [];

  answer.push(arr.findIndex((element, index) => element >= arr[index + 1]) + 1);
  answer.push(
    arr.findLastIndex((element, index) => element >= arr[index - 1] + 1)
  );

  return answer;
}

