function solution(meeting) {
  let answer = 0;

  meeting.sort((a, b) => a[0] - b[0] || a[1] - b[1]);

  for (let i = 0; i < meeting.length; i++) {
    let count = 1;
    let endTime = meeting[i][1];

    for (let j = i + 1; j < meeting.length; j++) {
      if (meeting[j][0] < endTime) {
        continue;
      }

      endTime = meeting[j][1];
      count++;
    }

    if (answer < count) {
      answer = count;
    }

    if (answer >= meeting.length - i) {
      break;
    }
  }

  return answer;
}

